export type MessageKey = keyof (typeof MESSAGES)["en"];

function formatMessage(template: string, params?: MessageParams): string {
  if (!params) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key];
    return value == null ? "" : String(value);
  });
}

export function getCurrentLocale() {
  return useLocaleStore.getState().locale;
}

export function getIntlLocale(): string {
  return getCurrentLocale() === "ru" ? "ru-RU" : "en-US";
}

export function getMessage(key: MessageKey, params?: MessageParams): string {
  const locale = getCurrentLocale();
  return formatMessage(MESSAGES[locale][key] ?? MESSAGES.en[key], params);
}

export function useT() {
  const locale = useLocaleStore((state) => state.locale);
  return (key: MessageKey, params?: MessageParams): string =>
    formatMessage(MESSAGES[locale][key] ?? MESSAGES.en[key], params);
}
